			<h1>Instrucciones para los autores</h1>
			<p>Los cursos y talleres cortos servirán para fortalecer la práctica profesional, a través del uso de  tecnologías o sistemas aplicados a las enseñanzas de las matemáticas. En los talleres, el ponente dispondrá de dos sesiones con una duración de 2 horas cada una para la exposición de su trabajo.</p>
			<p>Un autor o coautor sólo podrá participar en un taller o curso corto.</p>
			<p>La propuesta debe contener:</p>
			<p>Título: Máximo 15 palabras.</p>
			<p>Contenido: Describir en 300 palabras como máximo el objetivo del curso o taller corto y las actividades que realizará en dos sesiones.</p>
			<p>Materiales requeridos.</p>
