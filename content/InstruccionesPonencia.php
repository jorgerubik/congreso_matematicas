			<h1>Instrucciones para los autores en la elaboración de resúmenes</h1>
			<p>La ponencia oral o cartel  debe ser original y producto de investigación teórica o aplicada. Serán aceptadas todos los trabajos que puedan hacer alguna aportación al conocimiento teórico o práctico en la enseñanza y aplicación de las matemáticas. Así mismo se debe cumplir con los lineamientos que se indican en la elaboración del resumen, de lo contrario el trabajo no será aceptado.
			El resumen deberá contener:</p>
			1. Título  (Máximo 15 palabras)<br>
			2. Nombre de autor y coautores sin grados académicos <b>(Máximo 5 integrantes)</b><br>
			3. Contenido máximo 300 palabras estructurado de acuerdo a la categoría elegida (Investigación o experiencia en aula)<br>
			4. Referencias estilo APA (American Psychological Association), máximo 30 palabras.<br>	
			<p><strong>a) Investigación </strong></p>	
			<p>En este apartado se incluirán todas las aportaciones inéditas de investigación científica en enseñanza y aplicación de las matemáticas, concluidas o en proceso, con  resultados o avances, sustentados con evidencias.</p>
			<p>En el resumen se expondrá una síntesis del tema referido, se debe incluir objetivo, sustento teórico, metodología, resultados y conclusiones. </p>	
			<p><strong>b) Experiencia en aula</strong></p>
			<p>En esta parte el autor denotará sus experiencias vividas en el aula a través de la modificación de estrategias didácticas o implementadas bajo un sustento teórico que permita el aprendizaje significativo en el alumno.</p>
			<p>En el resumen se expondrá una síntesis del tema referido, se debe incluir objetivo, antecedentes, desarrollo donde se incluyan las estrategias empleadas o modificadas, resultados y conclusiones. </p>
			<h1>Registro de ponentes y resumen</h1>
			El ponente podrá participar al máximo en cinco presentaciones ya sea como autor o coautor en: ponencias, curso corto, taller o cartel.<br>
			Las constancias son individuales por lo que el autor y coautores que requieran constancia de participación es necesario se registren y realicen el pago correspondiente.<br>
			Se entregará constancia de asistencia al congreso por 30 h, cuando menos con una asistencia de 70%. <br>
